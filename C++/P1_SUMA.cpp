#include <iostream>
using namespace std;

int main() {
    int num1, num2, suma;

    // Se solicita numeros para ingresar
    cout << "Ingresa el primer numero: ";
    cin >> num1;

    cout << "Ingresa el segundo numero: ";
    cin >> num2;

    // Se realiza la suma
    suma = num1 + num2;

    // Se muestra el resultado
    cout << "La suma de los dos numeros es: " << suma << endl;

    return 0;
}
